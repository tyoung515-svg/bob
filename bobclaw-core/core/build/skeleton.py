"""BoBClaw build pipeline — deterministic codegen + the build-empty gate.

Ported from ``demo_variant_b.py``. Given a validated contract list (see
:mod:`core.build.contracts`) and an impl map, this writes a runnable package:

    <workspace>/
        minikit/__init__.py        re-exports functions
        minikit/functions.py       HEADER + one body per contract (impl or stub)
        minikit/cli.py             runs a sample of contracts (a "it runs" smoke)
        tests/test_functions.py    one test per contract (the honest gate)

``write_app`` is deterministic: same contracts + impls → byte-identical files. The
build-empty gate (:func:`build_empty_ok` + :func:`collect_tests`) runs BEFORE any
worker fills a contract — it proves the skeleton imports clean and the tests
collect, so "builds" is guaranteed and the only honest variable is the pass/fail
count once impls land.

The subprocess helpers here run ``sys.executable`` against the *workspace* dir only
(``PYTHONPATH`` points at it); they do NOT touch the network. P3 hardens the
subprocess env + path containment further — this module deliberately keeps the
minimal proven shape from the demo.
"""
from __future__ import annotations

import os
import re
import subprocess
import sys
from pathlib import Path

from core.build.contracts import ALLOWED_IMPORTS

# The package the generated app lives in. Hard-coded to the demo's "minikit" target
# (the P4 live E2E builds a small minikit); a future param could derive it per-job.
PACKAGE_NAME = "minikit"

# Auto-generated import header prepended to functions.py — supplies every stdlib
# module in ALLOWED_IMPORTS so generated bodies need no per-function imports.
HEADER = (
    f"# AUTO-GENERATED by core.build.skeleton ({PACKAGE_NAME})\n"
    # PEP 563: annotations become strings, never evaluated at import — defense in depth
    # against a code-execution expression in a function annotation (the contract gate
    # also screens signatures via contracts.is_safe_impl).
    "from __future__ import annotations\n"
    f"import {', '.join(ALLOWED_IMPORTS)}\n\n\n"
)

# Subprocess wall-clock timeouts (s) — ported from the demo. Generous enough for a
# cold import / collect on a loaded dev box, bounded so a hang can't wedge a turn.
_BUILD_TIMEOUT = 60
_COLLECT_TIMEOUT = 120
_PYTEST_TIMEOUT = 300   # default for run_pytest; the verify node overrides via config
_CLI_TIMEOUT = 60


def merge_impls(entries: list[dict]) -> dict[str, str]:
    """Merge ``build_impls`` entries → ``{name: source}``, last non-None source wins.

    Iterates in INSERTION order (``build_impls`` is an ``operator.add`` reducer, so
    fan-out worker entries precede any repair entries, and a later repair pass's fix
    supersedes the original impl). Each worker name is unique, so the worker order
    being non-deterministic doesn't affect the result; only a same-name repair entry
    overrides, deterministically. Entries with no source keep the contract's stub.
    """
    impls: dict[str, str] = {}
    for e in entries:
        src = e.get("source")
        if src:
            impls[e["name"]] = src
    return impls


def stub(unit: dict) -> str:
    """A NotImplementedError stub for an unfilled contract (imports clean, fails if run)."""
    return f"def {unit['signature']}:\n    raise NotImplementedError  # {unit['name']}\n"


def render_test(unit: dict) -> str:
    """Render one pytest function for a contract from its declared cases.

    No cases → ``assert callable(name)`` (a contract with no examples still proves it
    exists). Each case with ``expect`` → ``assert name(*args) == expect``; a case
    without ``expect`` just calls it (a smoke that it doesn't raise). Values are
    rendered with ``repr`` so the generated source is self-contained.
    """
    name = unit["name"]
    out = [f"def test_{name}():"]
    cases = unit.get("cases") or []
    if not cases:
        out.append(f"    assert callable({name})")
    for c in cases:
        args = ", ".join(repr(a) for a in c.get("args", []))
        call = f"{name}({args})"
        if "expect" in c:
            out.append(f"    assert {call} == {c['expect']!r}")
        else:
            out.append(f"    {call}")
    return "\n".join(out) + "\n"


def write_app(app_dir: Path, units: list[dict], impls: dict[str, str]) -> None:
    """(Re)generate the package + tests + CLI from contracts + impls (deterministic).

    A contract with no impl gets its :func:`stub`, so the package ALWAYS imports
    cleanly regardless of how many workers succeeded. Called once for the empty
    skeleton (``impls={}``) and again after each fill/repair wave.
    """
    app_dir = Path(app_dir)
    pkg = app_dir / PACKAGE_NAME
    pkg.mkdir(parents=True, exist_ok=True)
    (app_dir / "tests").mkdir(exist_ok=True)

    bodies = [impls.get(u["name"]) or stub(u) for u in units]
    (pkg / "functions.py").write_text(HEADER + "\n\n".join(bodies) + "\n", encoding="utf-8")
    (pkg / "__init__.py").write_text(
        f"from {PACKAGE_NAME}.functions import *  # noqa\n", encoding="utf-8"
    )

    tests = [f"from {PACKAGE_NAME}.functions import *  # noqa\n\n"] + [
        render_test(u) for u in units
    ]
    (app_dir / "tests" / "test_functions.py").write_text("\n".join(tests), encoding="utf-8")

    # Runnable CLI: exercise a sample of the functions with their first test case.
    sample = [u for u in units if u.get("cases")][:8]
    cli = [
        f"from {PACKAGE_NAME} import functions as F\n\n",
        "def main():\n",
        f"    print('{PACKAGE_NAME} demo - running sample functions:')\n",
    ]
    for u in sample:
        args = ", ".join(repr(a) for a in u["cases"][0].get("args", []))
        cli.append(f"    print('  {u['name']}(...) ->', repr(F.{u['name']}({args})))\n")
    cli.append("\nif __name__ == '__main__':\n    main()\n")
    (pkg / "cli.py").write_text("".join(cli), encoding="utf-8")


# Env keys the build/test subprocess legitimately needs (Python + pytest, Win + POSIX).
# P3 hardening: the subprocess EXECUTES generated, LLM-written code (pytest imports
# functions.py and calls the impls), so it runs with an ALLOWLISTED env — every other
# variable, NOTABLY secrets (``*_API_KEY`` / ``BOBCLAW_SECRET`` / the .secrets file's
# vars), is stripped so a malicious/buggy impl cannot read credentials out of os.environ.
_SAFE_ENV_KEYS: frozenset[str] = frozenset({
    "PATH", "SYSTEMROOT", "SystemRoot", "WINDIR", "COMSPEC", "PATHEXT",
    "TEMP", "TMP", "TMPDIR", "NUMBER_OF_PROCESSORS", "PROCESSOR_ARCHITECTURE",
    "HOME", "LANG", "LC_ALL", "LC_CTYPE", "USERPROFILE", "APPDATA", "LOCALAPPDATA",
    "SYSTEMDRIVE", "HOMEDRIVE", "HOMEPATH",
})


def build_env(app_dir: Path) -> dict:
    """A CONSTRAINED subprocess env: the app on PYTHONPATH + only the allowlisted system
    vars Python/pytest need — NO inherited secrets (P3 sandbox hardening). The generated
    code runs here, so it must not see API keys / tokens from the parent process.

    SCOPE OF THIS DEFENSE: it strips ENV-borne secrets only. It does NOT jail the
    filesystem or network — code that slips the static gate (contracts.is_safe_impl)
    can still read on-disk secrets by absolute path and open sockets with the user's
    privileges. OS-level isolation is the durable boundary (see is_safe_impl)."""
    env = {k: v for k, v in os.environ.items() if k in _SAFE_ENV_KEYS}
    env["PYTHONPATH"] = str(app_dir)
    env["PYTHONIOENCODING"] = "utf-8"
    env["PYTHONDONTWRITEBYTECODE"] = "1"
    return env


def build_empty_ok(app_dir: Path) -> bool:
    """True iff the generated package imports cleanly in a fresh subprocess.

    The deterministic "builds" guarantee: stubs raise only when CALLED, so a clean
    import means the skeleton is structurally sound (all signatures compiled). A
    False here is fail-loud — it means our codegen or a parser-admitted signature is
    broken, never a worker's fault.
    """
    app_dir = Path(app_dir)
    proc = subprocess.run(
        [sys.executable, "-c",
         f"import {PACKAGE_NAME}; "
         f"print(len([n for n in dir({PACKAGE_NAME}) if not n.startswith('_')]))"],
        cwd=str(app_dir), capture_output=True, text=True,
        env=build_env(app_dir), timeout=_BUILD_TIMEOUT,
    )
    return proc.returncode == 0


def collect_tests(app_dir: Path) -> int:
    """Number of tests pytest collects from the skeleton (``--collect-only``).

    Proves the suite is wired (one test per contract) before any impl lands. Returns
    0 if collection errors — surfaced by the caller, never silently treated as "no
    tests to run".
    """
    app_dir = Path(app_dir)
    proc = subprocess.run(
        [sys.executable, "-m", "pytest", "--collect-only", "-q",
         "-p", "no:cacheprovider", "tests"],
        cwd=str(app_dir), capture_output=True, text=True,
        env=build_env(app_dir), timeout=_COLLECT_TIMEOUT,
    )
    out = proc.stdout + proc.stderr
    m = re.search(r"(\d+) test", out)
    return int(m.group(1)) if m else 0


def run_pytest(app_dir: Path, *, timeout: int = _PYTEST_TIMEOUT) -> dict:
    """Run the app's pytest suite in a subprocess → pass/fail/error counts + names.

    Ported from the demo. ``failing`` is the set of FAILED test names (the contract
    names, since each test is ``test_<name>``) so the repair pass knows which units
    to fix. ``raw`` is the tail of output for the surfaced summary. This is the
    HONEST gate — the tests are generated from the contracts' declared cases and are
    NEVER edited to make a failure pass (a bad spec stays red and is surfaced).
    """
    app_dir = Path(app_dir)
    proc = subprocess.run(
        [sys.executable, "-m", "pytest", "-q", "--no-header",
         "-p", "no:cacheprovider", "tests"],
        cwd=str(app_dir), capture_output=True, text=True,
        env=build_env(app_dir), timeout=timeout,
    )
    return parse_pytest_summary(proc.stdout + proc.stderr)


def parse_pytest_summary(out: str) -> dict:
    """Parse pytest -q output → {passed, failed, errors, failing, raw}.

    Shared by the host runner (above) and the docker runner (core.build.sandbox) so the
    gate's pass/fail interpretation is identical regardless of where pytest executed.

    Counts are read from pytest's FINAL summary line ONLY (the last line carrying a
    count) — NOT a free-text search over the whole output, so a ``\\d+ passed/failed/
    error``-shaped substring inside a captured-stdout traceback or an impl's own logging
    can't corrupt the verdict (which could flip a red build green). ``failing`` is the
    set of FAILED and per-test ERROR names (= contract names) for the repair pass.
    """
    # The summary is the last line that mentions a pytest outcome count.
    summary = ""
    for ln in out.splitlines():
        s = ln.strip()
        if re.search(r"\d+\s+(passed|failed|error|skipped|xfailed|xpassed)\b", s):
            summary = s
    passed = int((re.search(r"(\d+) passed", summary) or [0, 0])[1])
    failed = int((re.search(r"(\d+) failed", summary) or [0, 0])[1])
    errors = int((re.search(r"(\d+) error", summary) or [0, 0])[1])
    # FAILED <id> and ERROR <id> short-summary lines both name the failing unit.
    failing = sorted(set(re.findall(
        r"::test_([a-zA-Z0-9_]+)\b",
        "\n".join(ln for ln in out.splitlines()
                  if ln.startswith(("FAILED", "ERROR")) or " FAILED" in ln or " ERROR" in ln),
    )))
    return {"passed": passed, "failed": failed, "errors": errors,
            "failing": failing, "raw": out[-1500:]}


def run_cli(app_dir: Path, *, timeout: int = _CLI_TIMEOUT) -> tuple[bool, str]:
    """Run the generated CLI (``python -m minikit.cli``) → (ran_ok, output tail).

    The "it actually runs" signal of the gate (the deterministic skeleton guarantees
    "builds"; pytest is the honest test gate; this proves the package executes)."""
    app_dir = Path(app_dir)
    proc = subprocess.run(
        [sys.executable, "-m", f"{PACKAGE_NAME}.cli"],
        cwd=str(app_dir), capture_output=True, text=True,
        env=build_env(app_dir), timeout=timeout,
    )
    return proc.returncode == 0, (proc.stdout + proc.stderr).strip()
